// Declarar diferentes tipos de variables y mostrar sus valores por consola.


// Declaración de una variable de tipo entero (número)
let numero = 42;
console.log("Número:", numero);

// Declaración de una variable de tipo decimal
let decimal = 3.14;
console.log("Decimal:", decimal);

// Declaración de una variable de tipo cadena (string)
let texto = "Hola, ¿Que haces?!";
console.log("Texto:", texto);

// Declaración de una variable de tipo booleano
let esVerdadero = true;
console.log("Booleano:", esVerdadero);

// Declaración de una variable de tipo indefinido
let indefinido;
console.log("Indefinido:", indefinido);

// Declaración de un valor nulo
let nulo = null;
console.log("Nulo:", nulo);